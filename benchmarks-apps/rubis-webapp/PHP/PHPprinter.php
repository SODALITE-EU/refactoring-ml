function authenticate($nickname, $password, $link)
{
  $result = mysqli_query($link, "SELECT id FROM users WHERE nickname=\"$nickname\" AND password=\"$password\"");
  if (!$result)
  {
	error_log("[".__FILE__."] Authentification query 'SELECT id FROM users WHERE nickname=\"$nickname\" AND password=\"$password\"' failed: " . mysqli_error($link));
	die("ERROR: Authentification query failed for user '$nickname': " . mysqli_error($link));
  }
  if (mysqli_num_rows($result) == 0)
    return -1;
  $row = mysqli_fetch_array($result);
  return $row["id"];
}

